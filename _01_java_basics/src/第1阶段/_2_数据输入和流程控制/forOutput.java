package 第1阶段._2_数据输入和流程控制;
// 在控制台输出1-5 和 5-1的数据
public class forOutput {
    public static void main(String[] args) {
        for(int i=1; i<6; i++) {
            System.out.println(i);
        }
        System.out.println("--------------------");
        for(int i=5; i>0; i--) {
            System.out.println(i);
        }
    }
}
